-- Rentetabellen: wettelijke rente en handelsrente apart in database
-- Vervangt de hardcoded RENTETABEL in rente_calculator.py

-- =============================================================================
-- TABELLEN
-- =============================================================================

CREATE TABLE rentetabel_wettelijk (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  ingangsdatum DATE NOT NULL UNIQUE,
  percentage DECIMAL(6,4) NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE rentetabel_handels (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  ingangsdatum DATE NOT NULL UNIQUE,
  percentage DECIMAL(6,4) NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================================================
-- RLS POLICIES
-- =============================================================================

ALTER TABLE rentetabel_wettelijk ENABLE ROW LEVEL SECURITY;
ALTER TABLE rentetabel_handels ENABLE ROW LEVEL SECURITY;

-- Read: alle authenticated users
CREATE POLICY "rentetabel_wettelijk_read" ON rentetabel_wettelijk
  FOR SELECT TO authenticated USING (true);

CREATE POLICY "rentetabel_handels_read" ON rentetabel_handels
  FOR SELECT TO authenticated USING (true);

-- Write: alleen admins (via service role key, geen RLS policy nodig voor insert/update/delete)
-- De backend gebruikt service_role key voor admin operaties

